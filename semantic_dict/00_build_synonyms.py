def build_synonyms_ruwordnet(indicator_names: list[str], stopwords: set) -> dict:
    """Попытка использовать ruwordnet; при неудаче — возврат встроенного словаря."""
    try:
        import ruwordnet
        wn = ruwordnet.RuWordNet()
        result: dict[str, list[str]] = {}
        for name in indicator_names:
            for tok in razdel.tokenize(name):
                t = tok.text.lower()
                lemma = _morph.parse(t)[0].normal_form
                if lemma in stopwords or len(lemma) < 3 or lemma in result:
                    continue
                synonyms: set[str] = set()
                for synset in wn.get_synsets(lemma):
                    for sense in synset.senses:
                        syn_lemma = _morph.parse(sense.name.lower())[0].normal_form
                        if syn_lemma != lemma and len(syn_lemma) > 2:
                            synonyms.add(syn_lemma)
                if synonyms:
                    result[lemma] = list(synonyms)[:5]
        return result
    except ImportError:
        print("ruwordnet недоступен, используется встроенный словарь", file=sys.stderr)
        return {}


def main():
    stopwords = set(STOPWORDS_PATH.read_text(encoding="utf-8").splitlines())

    ruwn_synonyms = build_synonyms_ruwordnet([], stopwords)

    result = dict(BUILTIN_SYNONYMS)
    result.update(ruwn_synonyms)

    OUT_PATH.write_text(json.dumps(result, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Синонимов лемм: {len(result)} → {OUT_PATH}")


if __name__ == "__main__":
    main()
